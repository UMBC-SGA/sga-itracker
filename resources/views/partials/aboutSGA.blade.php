            About the UMBC Student Government Association
        </h3>
    </div>
    <div class="box-body">
        <p>The UMBC Student Government Association (SGA) is the voice of the undergraduate students
            at UMBC. Serving as one of the governing bodies at UMBC, the SGA meets with key
            administrators, organizes and empowers the undergraduate student body, and approves
            campus-wide policies in support of an active, academically enriched, social student
            environment. The SGA has an Executive Branch, Senate, Finance Board, and Election Board.
            The entire undergraduate population makes up the foundation of the SGA.</p>
        <p>Every undergraduate student at UMBC is a member of the SGA. Elected officers and many
            appointed officers run programs, services and advocacy efforts with all undergraduates.
            SGA’s purpose is to organize and support undergraduate students in creating
            distinctive community, co-curricular and academic experiences; identify and voice
            students’ hopes and concerns; engage students in campus activities and
            decision-making; build mutually beneficial partnerships with individuals and
            organizations on and off campus and promote and defend students’ welfare.</p>
